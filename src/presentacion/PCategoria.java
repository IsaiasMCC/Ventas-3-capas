    public void listar() {
        ArrayList<NCategoria> lista = ncategoria.findAll();
        DefaultTableModel tablaCategoria = (DefaultTableModel) tableCategoria.getModel();
        tablaCategoria.setRowCount(0);
        lista.forEach((item) -> {
            tablaCategoria.addRow(new Object[]{
                item.id,
                item.nombre
            });
        });
    }
    
