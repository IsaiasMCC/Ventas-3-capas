    private void createButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createButtonActionPerformed
        String name = nameText.getText();
        this.ncategoria.create(name);
        this.listar();
        this.showAlert("Categoria Agregada!");
    }//GEN-LAST:event_createButtonActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        if (!idText.getText().isEmpty()) {
            int idSelected = Integer.parseInt(idText.getText());
            String name = nameText.getText();
            this.ncategoria.update(idSelected, name);
            this.listar();
            this.clear();
            this.showAlert("Categoria Actualizada!");
        } else {
            this.showAlert("Seleccione una categoria!");
        }
    }//GEN-LAST:event_updateButtonActionPerformed

    private void tableCategoriaMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableCategoriaMousePressed
        int row = tableCategoria.getSelectedRow();
        idText.setText( tableCategoria.getValueAt(row, 0).toString() );
        nameText.setText( tableCategoria.getValueAt(row, 1).toString() );
        
    }//GEN-LAST:event_tableCategoriaMousePressed

    private void clearButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_clearButtonActionPerformed
       this.clear();
    }//GEN-LAST:event_clearButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        if (!idText.getText().isEmpty()) {
            int idSelected = Integer.parseInt(idText.getText());
            String name = nameText.getText();
            this.ncategoria.delete(idSelected);
            this.listar();
            this.clear();
            this.showAlert("Categoria Eliminada!");
        } else {
            this.showAlert("Seleccione una categoria!");
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

