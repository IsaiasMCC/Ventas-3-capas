    private void listarProductos() {
        ArrayList<NProducto> lista = np.findAll();
        DefaultTableModel tablaProducto = (DefaultTableModel) tableProductos.getModel();
        tablaProducto.setRowCount(0);
        lista.forEach((item) -> {
            tablaProducto.addRow(new Object[]{
                item.id,
                item.nombre,
                item.precio,
                item.categoriaId
            });
        });
    }
    
    private void showAlert(String mensaje) {
        JOptionPane.showMessageDialog(null, mensaje, "Alerta", JOptionPane.INFORMATION_MESSAGE);
    }

    private DefaultComboBoxModel toCombo(ArrayList<NCategoria> ls) {
        ComboBoxItem[] le = new ComboBoxItem[ls.size()];
        for (int i = 0; i < ls.size(); i++) {
            NCategoria e = ls.get(i);
            le[i] = new ComboBoxItem(Integer.toString(e.id), (String) e.nombre);
        }
        return new DefaultComboBoxModel(le);
    }

    private void clear() {
        idText.setText("");
        nameText.setText("");
        priceText.setText("");
    }

