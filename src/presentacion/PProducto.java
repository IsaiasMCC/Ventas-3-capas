    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = tableProductos.getSelectedRow();
        if (row != -1) {
            int idSelected = Integer.parseInt(idText.getText());
            String name = nameText.getText();
            float price = Float.parseFloat(priceText.getText());
            int categoriaId = Integer.parseInt(((ComboBoxItem) categoriaBox.getSelectedItem()).key);
            this.np.update(idSelected, name, price, categoriaId);
            this.listarProductos();
            this.clear();
        } else {
            showAlert("Seleccione un producto!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int row = tableProductos.getSelectedRow();
        if (row != -1) {
            int idSelected = Integer.parseInt(idText.getText());
            this.np.delete(idSelected);
            this.listarProductos();
            this.clear();
        } else {
            showAlert("Seleccione un producto!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

